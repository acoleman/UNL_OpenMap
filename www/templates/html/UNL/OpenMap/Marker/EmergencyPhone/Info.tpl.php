<p>The University of Nebraska-Lincoln operates nearly 50 Emergency
Blue Phone Poles. They are placed near parking lots, academic buildings,
intersections and walk ways for easy access on both the City and East
Campuses.</p>

<p>The purpose of the phones is to notify the University Police of
an emergency. University Operators monitor the phones and inform
emergency service personnel of the situation. UNL Police Officers,
Community Service Officers and Lincoln Fire Department Personnel can
then be dispatched accordingly once a call comes in. Officers will still
respond even if there is no audio present when the phone is activated.</p>

