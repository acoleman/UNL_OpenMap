<?php
class UNL_TourMap_LatLng
{
    public $lat;
    public $lng;

    function __construct($options = array())
    {
        if (isset($options['lat'])) {
            $this->lat = $options['lat'];
        }
        if (isset($options['lng'])) {
            $this->lng = $options['lng'];
        }
    }
}